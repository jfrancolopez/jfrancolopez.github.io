import { writeFileSync } from 'node:fs';
import { execFileSync } from 'node:child_process';
import { resume } from '../src/data/resume.js';

const htmlPath = 'tools/FrancoLopez-Resume.html';
const pdfPath = 'public/FrancoLopez-Resume.pdf';

const escapeHtml = (value) =>
  String(value)
    .replaceAll('&', '&amp;')
    .replaceAll('<', '&lt;')
    .replaceAll('>', '&gt;')
    .replaceAll('"', '&quot;')
    .replaceAll("'", '&#039;');

const list = (items) => `
    <ul>
      ${items.map((item) => `<li>${escapeHtml(item)}</li>`).join('\n      ')}
    </ul>`;

const projectList = (items) => `
    <ul>
      ${items
        .map((project) => `<li><strong>${escapeHtml(project.name)}:</strong> ${escapeHtml(project.description)}</li>`)
        .join('\n      ')}
    </ul>`;

const skills = resume.skills
  .map((skill) => `<strong>${escapeHtml(skill.label)}:</strong> ${escapeHtml(skill.items.join(', '))}`)
  .join('<br />\n      ');

const experience = resume.experience
  .map(
    (job) => `
    <div class="role">
      <h3>${escapeHtml(job.company)} · ${escapeHtml(job.role)}</h3>
      <p class="date">${escapeHtml(job.dates)}</p>
    </div>
    <p class="location">${escapeHtml(job.location)}</p>
    ${list(job.bullets)}`
  )
  .join('\n');

const contact = [
  resume.location,
  resume.availability,
  resume.languages.join(' / '),
  resume.contact.email,
  resume.contact.linkedin,
  resume.contact.github,
].join(' · ');

const html = `<!doctype html>
<!-- Generated from src/data/resume.js. Do not edit this file by hand. -->
<html lang="en">
  <head>
    <meta charset="utf-8" />
    <title>${escapeHtml(resume.name)} Resume</title>
    <style>
      * { box-sizing: border-box; }
      body {
        margin: 0;
        padding: 34px;
        color: #17201d;
        font-family: Arial, Helvetica, sans-serif;
        font-size: 12px;
        line-height: 1.36;
      }
      h1, h2, h3, p { margin: 0; }
      h1 { font-size: 28px; letter-spacing: 0; }
      h2 {
        margin-top: 15px;
        border-bottom: 1px solid #c8cfc9;
        padding-bottom: 3px;
        color: #0b5d52;
        font-size: 13px;
        text-transform: uppercase;
      }
      h3 { margin-top: 9px; font-size: 13px; }
      .headline { margin-top: 4px; font-size: 14px; font-weight: 700; }
      .contact, .date, .location { color: #4a5550; }
      .contact { margin-top: 4px; }
      .summary { margin-top: 8px; }
      ul { margin: 5px 0 0 18px; padding: 0; }
      li { margin-bottom: 3px; }
      .role { display: flex; justify-content: space-between; gap: 12px; }
      .date { white-space: nowrap; }
      .skills strong { color: #17201d; }
    </style>
  </head>
  <body>
    <h1>${escapeHtml(resume.name)}</h1>
    <p class="headline">${escapeHtml(resume.pdfHeadline)}</p>
    <p class="contact">${escapeHtml(contact)}</p>

    <p class="summary">${escapeHtml(resume.pdfSummary)}</p>

    <h2>Technical Focus</h2>
    <p class="skills">
      ${skills}
    </p>

    <h2>Selected Platform and Infrastructure Work</h2>
    ${list(resume.selectedWork)}

    <h2>Experience</h2>
    ${experience}

    <h2>Projects</h2>
    ${projectList(resume.projects)}

    <h2>Education and Certifications</h2>
    ${list(resume.educationAndCertifications)}
  </body>
</html>
`;

writeFileSync(htmlPath, html);
execFileSync('wkhtmltopdf', [htmlPath, pdfPath], { stdio: 'inherit' });
